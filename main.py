from fastapi import FastAPI, UploadFile, Form
from fastapi.responses import FileResponse
from fastapi.middleware.cors import CORSMiddleware
import shutil
import pathlib
import os
import mimetypes

from process_plan import plan_from_llm, process_plan  # LLM logic

app = FastAPI()

# Enable CORS for frontend access
app.add_middleware(
    CORSMiddleware,
    allow_origins=["http://localhost:3000"],  # change for deployment
    allow_credentials=True,
    allow_methods=["*"],
    allow_headers=["*"],
)

@app.post("/process")
async def process_files(user_prompt: str = Form(...), files: list[UploadFile] = []):
    print("🔔 API called with user_prompt:", user_prompt)
    print(f"🔔 Received {len(files)} file(s)")

    # Prepare input/output folders
    src_dir = pathlib.Path("./web_input")
    out_dir = pathlib.Path("./web_output")
    src_dir.mkdir(exist_ok=True)
    out_dir.mkdir(exist_ok=True)

    # Clean up old files
    for f in src_dir.glob("*"):
        try:
            f.unlink()
        except Exception as e:
            print(f"⚠️ Could not delete {f}: {e}")
    for f in out_dir.glob("*"):
        try:
            f.unlink()
        except Exception as e:
            print(f"⚠️ Could not delete {f}: {e}")

    # Save uploaded files
    for upload in files:
        filename = pathlib.Path(upload.filename).name
        file_path = src_dir / filename
        with open(file_path, "wb") as buffer:
            shutil.copyfileobj(upload.file, buffer)
        print(f"💾 Saved and moved file to {file_path}")

    # Generate and execute LLM plan
    plan = plan_from_llm(user_prompt)
    print(f"💡 Plan generated by LLM: {plan}")
    results = process_plan(plan, src_dir, out_dir)
    print("✅ Process results:", results)

